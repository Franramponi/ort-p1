package ar.edu.ort.p1.u2.ejemplos._04_constructores.src;

/**
 * Creamos un constructor sin argumentos.
 */

class Bicicleta {

	/* Constructor default */
	Bicicleta() {
		System.out.println("La bicicleta fue creada");
	}

	/* Metodo main */
	public static void main(String args[]) {

		Bicicleta b = new Bicicleta();// calling a default constructor

	}
}