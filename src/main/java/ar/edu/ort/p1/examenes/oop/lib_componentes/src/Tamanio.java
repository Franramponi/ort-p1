package ar.edu.ort.p1.examenes.oop.lib_componentes.src;

public class Tamanio {
	private int ancho;
	private int alto;

	public Tamanio(int ancho, int alto) {
		super();
		this.ancho = ancho;
		this.alto = alto;
	}

	public int getAncho() {
		return ancho;
	}

	public void setAncho(int ancho) {
		this.ancho = ancho;
	}

	public int getAlto() {
		return alto;
	}

	public void setAlto(int alto) {
		this.alto = alto;
	}

}
